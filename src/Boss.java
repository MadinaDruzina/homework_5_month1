public class Boss {

    private int healthBoss;

    private int hitBoss;

    private String typeOfDef;

    public int getHealthBoss() {
        return healthBoss;
    }

    public void setHealthBoss(int healthBoss) {
        this.healthBoss = healthBoss;
    }

    public int getHitBoss() {
        return hitBoss;
    }

    public void setHitBoss(int hitBoss) {
        this.hitBoss = hitBoss;
    }

    public String getTypeOfDef() {
        return typeOfDef;
    }

    public void setTypeOfDef(String typeOfDef) {
        this.typeOfDef = typeOfDef;
    }
}
